// create a const object in javascript. can you change it to hold a number later 
const myObject = {
    property1: "value1",
    property2: "value2"
  };
  
  // Display the original object
  console.log(myObject); // Output: { property1: 'value1', property2: 'value2' }
  
  // Change a property to hold a number
  myObject.property1 = 42;
  
  // Display the modified object
  console.log(myObject); // Output: { property1: 42, property2: 'value2' }
  