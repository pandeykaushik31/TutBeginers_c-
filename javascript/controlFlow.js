// If statement
let hour = 15;

if (hour < 12) {
  console.log("Good morning!");
} else if (hour < 18) {
  console.log("Good afternoon!");
} else {
  console.log("Good evening!");
}

// While loop
let count = 0;

while (count < 5) {
  console.log(`Count is ${count}`);
  count++;
}

// For loop
for (let i = 0; i < 5; i++) {
  console.log(`Index is ${i}`);
}
