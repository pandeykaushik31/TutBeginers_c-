let num1 = 10;
let num2 = 5;

// Arithmetic Operators
let sum = num1 + num2; // Addition
let difference = num1 - num2; // Subtraction
let product = num1 * num2; // Multiplication
let quotient = num1 / num2; // Division

// Comparison Operators
let isEqual = num1 === num2; // Strict equality
let isNotEqual = num1 !== num2; // Strict inequality
let isGreaterThan = num1 > num2; // Greater than
let isLessThan = num1 < num2; // Less than

// Logical Operators
let andResult = (num1 > 0) && (num2 > 0); // Logical AND
let orResult = (num1 > 0) || (num2 > 0); // Logical OR
let notResult = !(num1 > 0); // Logical NOT
